CREATE SEQUENCE SQ_MICRO_CLIMA
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;

CREATE TABLE TB_MICRO_CLIMA(
    ID_CLIMA INTEGER DEFAULT SQ_MICRO_CLIMA.NEXTVAL NOT NULL,
    ID_TORNADO INTEGER NOT NULL,
    DS_CLIMA VARCHAR2(100) NOT NULL,
    VL_TEMPERATURA INTEGER NOT NULL,
    DATA_CLIMA DATE DEFAULT SYSDATE,
    DS_LOCAL VARCHAR2(100) NOT NULL
);