CREATE SEQUENCE SQ_MICRO_TORNADO
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;

CREATE TABLE TB_MICRO_TORNADO(
    ID_TORNADO INTEGER DEFAULT SQ_MICRO_TORNADO.NEXTVAL NOT NULL,
    NOME_TORNADO VARCHAR2(100) NOT NULL,
    ClASSIFICACAO VARCHAR2(30) NOT NULL,
    LOCAL_ORIGEM VARCHAR2(100) NOT NULL,
    DATA_ORIGEM DATE DEFAULT SYSDATE
);